package Java_Array;

public class Single_array_2 
{
	public static void main(String[] args) {
		
		int ar [] = {12,19,23};
		
		System.out.println(ar.length);
		
		System.out.println(ar[2]);
		
		System.out.println("--------------");
		
		for(int i=0; i<=ar.length-1;i++)
		{
			System.out.println(ar[i]);
		}
		
		
	}

}
